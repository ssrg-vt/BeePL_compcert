(** Translation from Mach to eBPF assembly language *)

Require Archi.
Require Import Coqlib Errors.
Require Import AST Integers Memdata.
Require Import Op Locations Mach Asm.

Local Open Scope string_scope.
Local Open Scope error_monad_scope.

(** The code generation functions take advantage of several
  characteristics of the [Mach] code generated by earlier passes of the
  compiler, mostly that argument and result registers are of the correct
  types.  These properties are true by construction, but it's easier to
  recheck them during code generation and fail if they do not hold. *)

(** Extracting integer registers. *)

Definition ireg_of (r: mreg) : res ireg :=
  match preg_of r with
  | IR mr => OK mr
  | FR mr => Error (msg "eBPF does not have floating point registers")
  | _ => Error (msg "Asmgen.ireg_of")
  end.

Definition freg_of (r: mreg) : res freg :=
  match preg_of r with
  | FR mr => OK mr
  | _ => Error (msg "Asmgen.freg_of")
  end.

(** Smart constructors for arithmetic operations. *)

Definition addptrofs (rd rs: ireg) (n: ptrofs) (k: code) :=
  if Archi.ptr64
  then Error (msg "ebpf64 is not supported yet")
  else
    OK
      (if Ptrofs.eq_dec n Ptrofs.zero then
         Palu MOV W32 rd (inl rs) :: k
       else
         Palu MOV W32 rd (inl rs) ::
              Palu ADD W32 rd (inr (Imm32 (Ptrofs.to_int n))) :: k).

(** Translation of conditional branches. *)

Definition transl_comparison (cmp:comparison) (sg:Ctypes.signedness) :=
  match cmp with
  | Ceq => EQ
  | Cne => NE
  | Clt => LT sg
  | Cle => LE sg
  | Cgt => GT sg
  | Cge => GE sg
  end.

Definition transl_cbranch_signed (cmp: comparison) (w:width) (r1: ireg) (r2: ireg+immw w) (lbl: label) :=
  Pjmpcmp (transl_comparison cmp Ctypes.Signed) w  r1 r2 lbl.

Definition transl_cbranch_unsigned (cmp: comparison) (w:width) (r1: ireg) (r2: ireg+immw w) (lbl: label) :=
  Pjmpcmp (transl_comparison cmp Ctypes.Unsigned) w r1 r2 lbl.

Definition transl_cbranch (cond: condition) (args: list mreg) (lbl: label) (k: code) :=
  match cond, args with
  | Ccomp c, a1 :: a2 :: nil =>
      do r1 <- ireg_of a1;
      do r2 <- ireg_of a2;
      OK (transl_cbranch_signed c W32 r1 (inl r2) lbl :: k)

  | Ccompu c, a1 :: a2 :: nil =>
      do r1 <- ireg_of a1;
      do r2 <- ireg_of a2;
      OK (transl_cbranch_unsigned c W32 r1 (inl r2) lbl :: k)

  | Ccompimm c n, a1 :: nil =>
      do r1 <- ireg_of a1;
      OK (transl_cbranch_signed c W32 r1 (inr (Imm32 n)) lbl :: k)

  | Ccompuimm c n, a1 :: nil =>
      do r1 <- ireg_of a1;
      OK (transl_cbranch_unsigned c W32 r1 (inr (Imm32 n)) lbl :: k)

  | Ccompf _, _
  | Cnotcompf _, _
  | Ccompfs _, _
  | Cnotcompfs _, _ => Error (msg "Floating point comparisons are not available in eBPF")

  | _, _ => Error(msg "Asmgen.transl_cbranch")
  end.

Definition transl_cond_as_Pcmp (cond: comparison) (w:width) (sg:Ctypes.signedness) (rd:ireg) (a:ireg+immw w)  (k:code) :=
    Pcmp (transl_comparison cond sg) w rd a :: k.



Definition transl_cond_op (cond: condition) (r: mreg) (args: list mreg) (k: code) :=
  match cond, args with
  | Ccomp c, a1 :: a2 :: nil =>
      assertion (mreg_eq r a1);
      do r1 <- ireg_of a1;
      do r2 <- ireg_of a2;
      OK (transl_cond_as_Pcmp c W32 Ctypes.Signed r1 (inl r2) k)

  | Ccompu c, a1 :: a2 :: nil =>
      assertion (mreg_eq r a1);
      do r1 <- ireg_of a1;
      do r2 <- ireg_of a2;
      OK (transl_cond_as_Pcmp c W32 Ctypes.Unsigned r1 (inl r2) k)

  | Ccompimm c n, a1 :: nil =>
      assertion (mreg_eq r a1);
      do r1 <- ireg_of a1;
      OK (transl_cond_as_Pcmp c W32 Ctypes.Signed r1 (inr (Imm32 n)) k)

  | Ccompuimm c n, a1 :: nil =>
      assertion (mreg_eq r a1);
      do r1 <- ireg_of a1;
      OK (transl_cond_as_Pcmp c W32 Ctypes.Unsigned r1 (inr (Imm32 n)) k)

  | Ccompf c, _
  | Cnotcompf c, _
  | Ccompfs c, _
  | Cnotcompfs c, _ => Error (msg "Floating point comparisons are not available in eBPF")

  | _, _ => Error (msg "Asmgen.transl_cond_op")
  end.

(** Translation of the arithmetic operation [r <- op(args)].
  The corresponding instructions are prepended to [k]. *)

Definition warchi := if Archi.ptr64 then W64 else W32.


Definition transl_op (op: operation) (args: list mreg) (res: mreg) (k: code) :=
  match op, args with
  | Omove, a1 :: nil =>
      do r <- ireg_of res;
      do r1 <- ireg_of a1;
      OK (Palu MOV warchi r (inl r1) :: k)

  | Ointconst n, nil =>
      do r <- ireg_of res;
      OK (Palu MOV W32 r (inr (Imm32 n)) :: k)

  | Olongconst n, nil =>
      do r <- ireg_of res;
      if Int64.cmpu Cle n (Int64.repr Int.max_unsigned)
      then
        OK (Palu MOV W64 r (inr  (Imm64 n)) :: k)
      else Error (msg "out-of-bound constant")

  | Oaddrstack n, nil =>
      do r <- ireg_of res;
      addptrofs r SP n k

  | Oadd, a1 :: a2 :: nil =>
      assertion (mreg_eq a1 res);
      do r <- ireg_of res;
      do r2 <- ireg_of a2;
      OK (Palu ADD W32 r (inl r2) :: k)

  | Oaddimm n, a1 :: nil =>
      assertion (mreg_eq a1 res);
      do r <- ireg_of res;
      OK (Palu ADD W32 r (inr (Imm32 n)) :: k)

  | Oneg, a1 :: nil =>
      assertion (mreg_eq a1 res);
      do r <- ireg_of res;
      OK (Palu NEG W32 r (inl r) :: k)

  | Osub, a1 :: a2 :: nil =>
      assertion (mreg_eq a1 res);
      do r <- ireg_of res;
      do r2 <- ireg_of a2;
      OK (Palu SUB W32 r (inl r2) :: k)

  | Osubimm n, a1 :: nil =>
      assertion (mreg_eq a1 res);
      do r <- ireg_of res;
      OK (Palu SUB W32 r (inr (Imm32 n)) :: k)

  | Omul, a1 :: a2 :: nil =>
      assertion (mreg_eq a1 res);
      do r <- ireg_of res;
      do r2 <- ireg_of a2;
      OK (Palu MUL W32 r (inl r2) :: k)

  | Omulimm n, a1 :: nil =>
      assertion (mreg_eq a1 res);
      do r <- ireg_of res;
      OK (Palu MUL W32 r (inr (Imm32 n)) :: k)

  | Odivu, a1 :: a2 :: nil =>
      assertion (mreg_eq a1 res);
      do r <- ireg_of res;
      do r2 <- ireg_of a2;
      OK (Palu DIV W32 r (inl r2) :: k)

  | Odivuimm n, a1 :: nil =>
      assertion (mreg_eq a1 res);
      do r <- ireg_of res;
      OK (Palu DIV W32 r (inr (Imm32 n)) :: k)

  | Omodu, a1 :: a2 :: nil =>
      assertion (mreg_eq a1 res);
      do r <- ireg_of res;
      do r2 <- ireg_of a2;
      OK (Palu MOD W32 r (inl r2) :: k)

  | Omoduimm n, a1 :: nil =>
      assertion (mreg_eq a1 res);
      do r <- ireg_of res;
      OK (Palu MOD W32 r (inr (Imm32 n)) :: k)

  | Oand, a1 :: a2 :: nil =>
      assertion (mreg_eq a1 res);
      do r <- ireg_of res;
      do r2 <- ireg_of a2;
      OK (Palu AND W32 r (inl r2) :: k)

  | Oandimm n, a1 :: nil =>
      assertion (mreg_eq a1 res);
      do r <- ireg_of res;
      OK (Palu AND W32 r (inr (Imm32 n)) :: k)

  | Oor, a1 :: a2 :: nil =>
      assertion (mreg_eq a1 res);
      do r <- ireg_of res;
      do r2 <- ireg_of a2;
      OK (Palu OR W32 r (inl r2) :: k)

  | Oorimm n, a1 :: nil =>
      assertion (mreg_eq a1 res);
      do r <- ireg_of res;
      OK (Palu OR W32 r (inr (Imm32 n)) :: k)

  | Oxor, a1 :: a2 :: nil =>
      assertion (mreg_eq a1 res);
      do r <- ireg_of res;
      do r2 <- ireg_of a2;
      OK (Palu XOR W32 r (inl r2) :: k)

  | Oxorimm n, a1 :: nil =>
      assertion (mreg_eq a1 res);
      do r <- ireg_of res;
      OK (Palu XOR W32 r (inr (Imm32 n)) :: k)

  | Oshl, a1 :: a2 :: nil =>
      assertion (mreg_eq a1 res);
      do r <- ireg_of res;
      do r2 <- ireg_of a2;
      OK (Palu LSH W32 r (inl r2) :: k)

  | Oshlimm n, a1 :: nil =>
      assertion (mreg_eq a1 res);
      do r <- ireg_of res;
      OK (Palu LSH W32 r (inr (Imm32 n)) :: k)

  | Oshr, a1 :: a2 :: nil =>
      assertion (mreg_eq a1 res);
      do r <- ireg_of res;
      do r2 <- ireg_of a2;
      OK (Palu ARSH W32 r (inl r2) :: k)

  | Oshrimm n, a1 :: nil =>
      assertion (mreg_eq a1 res);
      do r <- ireg_of res;
      OK (Palu ARSH W32 r (inr (Imm32 n)) :: k)

  | Oshru, a1 :: a2 :: nil =>
      assertion (mreg_eq a1 res);
      do r <- ireg_of res;
      do r2 <- ireg_of a2;
      OK (Palu RSH W32 r (inl r2) :: k)

  | Oshruimm n, a1 :: nil =>
      assertion (mreg_eq a1 res);
      do r <- ireg_of res;
      OK (Palu RSH W32 r (inr (Imm32 n)) :: k)

  | Ocmp cmp, _ =>
      transl_cond_op cmp res args k

  (*c Following operations are not available in eBPF, and will throw errors in this step *)
| Oaddrsymbol s ofs, nil =>
    do r <- ireg_of res;
      OK (Ploadsymbol r s ofs :: k)

| Ocast8signed, a1 :: nil =>
    do r <- ireg_of res;
    assertion (mreg_eq a1 res);
OK (Palu LSH W32 r (inr (Imm32 (Int.repr 24))) ::
      Palu ARSH W32 r (inr (Imm32 (Int.repr 24))) :: k)
| Ocast16signed, a1 :: nil =>
    do r <- ireg_of res;
    assertion (mreg_eq a1 res);
OK (Palu LSH W32 r (inr (Imm32 (Int.repr 16))) ::
      Palu ARSH W32 r (inr (Imm32 (Int.repr 16))) :: k)

| Omulhs, a1 :: a2 :: nil => Error (msg "mulhs is not available in eBPF: pass -Os")
| Omulhu, a1 :: a2 :: nil => Error (msg "mulhu is not available in eBPF: pass -Os")
| Omod, a1 :: a2 :: nil => Error (msg "signed modulo is not available in eBPF")

| Oaddl , a1 ::a2 ::nil =>
    assertion (mreg_eq a1 res);
    do r <- ireg_of res;
    do r2 <- ireg_of a2;
    OK (Palu ADD W64 r (inl r2) :: k)
| Oaddlimm n , a1 :: nil =>
    assertion (mreg_eq a1 res);
    do r <- ireg_of res;
    OK (Palu ADD W64 r (inr (Imm64 n)) :: k)
| Onegl , a1:: nil =>
    assertion (mreg_eq a1 res);
    do r <- ireg_of res;
    OK (Palu NEG W64 r (inl r) :: k)

| Osubl , a1 ::a2 ::nil =>
    assertion (mreg_eq a1 res);
    do r <- ireg_of res;
    do r2 <- ireg_of a2;
    OK (Palu SUB W64 r (inl r2) :: k)

| Osublimm n , a1::nil =>
    assertion (mreg_eq a1 res);
    do r <- ireg_of res;
    OK (Palu SUB W64 r (inr (Imm64 n)) :: k)

| Omull  , a1 :: a2 :: nil =>
    assertion (mreg_eq a1 res);
    do r <- ireg_of res;
    do r2 <- ireg_of a2;
    OK (Palu MUL W64 r (inl r2) :: k)

| Omullimm n , a1::nil =>
    assertion (mreg_eq a1 res);
    do r <- ireg_of res;
    OK (Palu MUL W64 r (inr (Imm64 n)) :: k)

| Odivlu,  a1 :: a2 :: nil =>
      assertion (mreg_eq a1 res);
      do r <- ireg_of res;
      do r2 <- ireg_of a2;
      OK (Palu DIV W64 r (inl r2) :: k)

| Odivluimm  n, a1 :: nil =>
      assertion (mreg_eq a1 res);
      do r <- ireg_of res;
      OK (Palu DIV W64 r (inr (Imm64 n)) :: k)

| Omodlu, a1 :: a2 :: nil =>
      assertion (mreg_eq a1 res);
      do r <- ireg_of res;
      do r2 <- ireg_of a2;
      OK (Palu MOD W64 r (inl r2) :: k)

| Omodluimm  n, a1 :: nil =>
      assertion (mreg_eq a1 res);
      do r <- ireg_of res;
      OK (Palu MOD W64 r (inr (Imm64 n)) :: k)

| Oandl, a1 :: a2 :: nil =>
      assertion (mreg_eq a1 res);
      do r <- ireg_of res;
      do r2 <- ireg_of a2;
      OK (Palu AND W64 r (inl r2) :: k)

| Oandlimm n, a1 :: nil =>
      assertion (mreg_eq a1 res);
      do r <- ireg_of res;
      OK (Palu AND W64 r (inr (Imm64 n)) :: k)

  | Oorl, a1 :: a2 :: nil =>
      assertion (mreg_eq a1 res);
      do r <- ireg_of res;
      do r2 <- ireg_of a2;
      OK (Palu OR W64 r (inl r2) :: k)

  | Oorlimm n, a1 :: nil =>
      assertion (mreg_eq a1 res);
      do r <- ireg_of res;
      OK (Palu OR W64 r (inr (Imm64 n)) :: k)

| Oxorl, a1 :: a2 :: nil =>
      assertion (mreg_eq a1 res);
      do r <- ireg_of res;
      do r2 <- ireg_of a2;
      OK (Palu XOR W64 r (inl r2) :: k)

  | Oxorlimm n, a1 :: nil =>
      assertion (mreg_eq a1 res);
      do r <- ireg_of res;
      OK (Palu XOR W64 r (inr (Imm64 n)) :: k)

  | Oshll, a1 :: a2 :: nil =>
      assertion (mreg_eq a1 res);
      do r <- ireg_of res;
      do r2 <- ireg_of a2;
      OK (Palu LSH W64 r (inl r2) :: k)

  | Oshllimm n, a1 :: nil =>
      assertion (mreg_eq a1 res);
      do r <- ireg_of res;
      OK (Palu LSH W64 r (inr (Imm64 n)) :: k)

  | Oshrl, a1 :: a2 :: nil =>
      assertion (mreg_eq a1 res);
      do r <- ireg_of res;
      do r2 <- ireg_of a2;
      OK (Palu ARSH W64 r (inl r2) :: k)

  | Oshrlimm n, a1 :: nil =>
      assertion (mreg_eq a1 res);
      do r <- ireg_of res;
      OK (Palu ARSH W64 r (inr (Imm64 n)) :: k)

| Oshrlu      , _
| Oshrluimm _ , _ => Error (MSG "ebpf(64) does not support '" :: MSG (string_of_operation op) :: MSG "'" :: nil)

| Ofloatconst _, _
| Osingleconst _, nil

| Onegf, _
| Oabsf, _
| Oaddf, _
| Osubf, _
| Omulf, _
| Odivf, _

| Onegfs, _
| Oabsfs, _
| Oaddfs, _
| Osubfs, _
| Omulfs, _
| Odivfs, _

| Osingleoffloat, _
| Ofloatofsingle, _

| Ointoffloat, _
| Ointuoffloat, _
| Ofloatofint, _
| Ofloatofintu, _
| Ointofsingle, _
| Ointuofsingle, _
